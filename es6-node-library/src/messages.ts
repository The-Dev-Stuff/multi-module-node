/**
 * Logs a greeting message.
 * @param name - Name of the person to greet.
 */
export const greetFromEs6 = (name: string): string => {
  return `Hello, there ${name}!  Message from ES6 Module.`;
};

/**
 * Logs a farewell message.
 * @param name - Name of the person to say goodbye to.
 */
export const farewellFromEs6 = (name: string): string => {
  return `Goodbye from es6 module, ${name}!`;
};

