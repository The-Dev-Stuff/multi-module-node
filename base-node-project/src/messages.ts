/**
 * Logs a greeting message.
 * @param name - Name of the person to greet.
 */
export const greetFromBase = (name: string): string => {
  return `Hello, there ${name}! Message from base project.`;
}

/**
 * Logs a farewell message.
 * @param name - Name of the person to say goodbye to.
 */
export const farewellFromBase = (name: string): string => {
  return `Goodbye from base, ${name}!`;
}
